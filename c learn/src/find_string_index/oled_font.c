static inline uint16_t parent(uint16_t index)
{
	return (index == 0) ? 0 : (index - 1) >> 1;
}

static inline uint16_t left(uint16_t index)
{
	return (index << 1) + 1;
}

static inline uint16_t right(uint16_t index)
{
	return (index + 1) << 1;
}

static inline void swap(uint16_t *array, uint16_t index1, uint16_t index2)
{
	uint16_t tmp = array[index1];
	array[index1] = array[index2];
	array[index2] = tmp;
}

static inline int32_t font_strcmp(uint8_t *src1, uint8_t *src2)
{
#ifdef Encode_Type_GB2312
	return ((src1[0] << 8) + src1[1] - (src2[0] << 8) - src2[1]);
#elif defined Encode_Type_UTF8
	return ((src1[0] << 16) + (src1[1] << 8) + src1[2] - (src2[0] << 16) - (src2[1] << 8) - src2[2]);
#endif
}

// 堆排序辅助函数(从指定索引开始向下堆化)
static inline void Vector_SiftDown(uint16_t *array, uint16_t arrayNum, uint16_t index)
{
	while (1)
	{
		uint16_t left_index = index * 2 + 1;
		uint16_t right_index = left_index + 1;
		uint16_t max_index = index;
		if (left_index < arrayNum &&
			font_strcmp((uint8_t *)OLED_CF16x16[array[max_index]].chs_bytes, (uint8_t *)OLED_CF16x16[array[left_index]].chs_bytes) < 0)
			max_index = left_index;
		if (right_index < arrayNum &&
			font_strcmp((uint8_t *)OLED_CF16x16[array[max_index]].chs_bytes, (uint8_t *)OLED_CF16x16[array[right_index]].chs_bytes) < 0)
			max_index = right_index;
		if (max_index == index)
			break;
		swap(array, max_index, index);
		index = max_index;
	}
}

// 堆排序(heap sort)
void heap_sort(uint16_t *array, uint16_t arrayNum)
{
	// 建立大顶堆
	for (int32_t i = (arrayNum - 2) / 2; i >= 0; i--)
		Vector_SiftDown(array, arrayNum, i);
	for (int32_t i = arrayNum - 1; i > 0; i--)
	{
		// 交换首尾
		swap(array, 0, i);
		// 对数量为i的堆进行堆化(这样堆顶又是最大值)
		Vector_SiftDown(array, i, 0);
	}
}

uint16_t convertArrayToBST(uint16_t *nums, uint16_t root, int32_t start, int32_t end)
{
	// 找到中间位置
	uint16_t mid = (start + end) / 2;
	uint16_t left_index = left(root);
	uint16_t right_index = right(root);

	// 递归处理左右子树
	if (left_index < Oled_CF_Size)
		nums[left_index] = convertArrayToBST(nums, left_index, start, mid);
	if (right_index < Oled_CF_Size)
		nums[right_index] = convertArrayToBST(nums, right_index, mid + 1, end);
	return OLED_CF_Tree[mid];
}

/**
 * @brief 中文字体索引数组初始化,已经在OLED_Init中调用
 */
void oled_font_init(void)
{
	// 为每个节点赋值
	for (uint16_t i = 0; i < Oled_CF_Size; i++)
		OLED_CF_Tree[i] = i;
	uint16_t tmp_array[Oled_CF_Size];
	// 把数组按中文字符大小排序
	heap_sort(OLED_CF_Tree, Oled_CF_Size);
	// 构建二叉搜索树
	tmp_array[0] = OLED_CF_Tree[(Oled_CF_Size - 1) >> 1];
	convertArrayToBST(tmp_array, 0, 0, Oled_CF_Size - 1);
	if (Oled_CF_Size % 2 == 0)
		tmp_array[(Oled_CF_Size - 1) >> 1] = OLED_CF_Tree[tmp_array[(Oled_CF_Size - 1) >> 1] + 1];
	// 把排好序的值拷贝到原数组
	memcpy(OLED_CF_Tree, tmp_array, Oled_CF_Size * sizeof(uint16_t));
}

int32_t oled_find_chs_index(uint8_t *string)
{
	uint16_t i = 0;
	while (i < Oled_CF_Size)
	{
		int32_t cmp = font_strcmp(string, (uint8_t *)OLED_CF16x16[OLED_CF_Tree[i]].chs_bytes);
		if (cmp == 0)
			return i;
		else if (cmp > 0)
			i = right(i);
		else
			i = left(i);
	}
	return -1;
}

