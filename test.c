#include <stdio.h>
#include "string.h"
#include "stdlib.h"
/*---------------------------------------------------------------
众所周知，数字或字符串等在内存中以字节为单位进行存储，
对于一个十六进制数0x12345678,其占用字节数为4，
如果从低地址到高地址依次存储78h,56h,34h,12h那么称该存储方式（字节序）为小端序，
反之则为大端序，一般计算机采用小端序这样更容易处理符号位（补码），而网络序一般
为大端序，这样更如何人的自然阅读顺序
---------------------------------------------------------------*/
void f(void *a)
{
   printf("%d",sizeof(*a));
}
int main()
{
   int *a;
   f(a);
}